package desktoproject.Model.Classes.Transactions;

/**
 *
 * @author gabriel
 */
public class ServiceType {
    private int id;
    private String name;
    private float price;

    public ServiceType(String name, float price) {
        this.name = name;
        this.price = price;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
    
    
    
}
